function render(type = 'default') {
  const pixels = characters[type] || characters.default;
  let output = '\n';
  for (let y = 0; y < pixels.length; y++) {
    let line = '    ';
    for (let x = 0; x < pixels[y].length; x++) {
      const val = pixels[y][x];
      if (val === 0) line += '  ';
      else {
        const [r, g, b] = palette[val];
        line += `\x1b[48;2;${r};${g};${b}m  \x1b[0m`;
      }
    }
    output += line + '\n';
  }
  console.log(output);
  console.log(`     \x1b[1m\x1b[38;2;63;81;181m[ AnTeamJang - ${type.toUpperCase()} ]\x1b[0m\n`);
}

